int	ft_sqrt(int nb)
{
	int res;

	res = 0;
	if (nb <= 0)
		return (0);
	while (nb != 0)
	{
		while (res <= nb / 2)
		{
			if (res * res == nb)
				return (res);
			res++;
		}
		nb = nb - 1;
	}
	return (0);
}
